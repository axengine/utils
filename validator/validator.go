package validator

import (
	"regexp"
)

func IsE164(in string) bool {
	e164RegexString := "^\\+[1-9]?[0-9]{7,14}$"
	e164Regex := regexp.MustCompile(e164RegexString)
	return e164Regex.MatchString(in)
}

